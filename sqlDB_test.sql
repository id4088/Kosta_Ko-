CREATE TABLE USERTBL
(   user_ID    CHAR(5) NOT NULL PRIMARY KEY,
    user_Name   NVARCHAR2(10) NOT NULL,
    BirthYear   NUMBER(10) NOT NULL,
    Address     NCHAR(10) NOT NULL,
    Mobile1     CHAR(3),
    Mobile2     CHAR(8),
    Height      NUMBER(3),
    mDate       DATE
);

CREATE TABLE BUYTBL
(   idNum       NUMBER(8) PRIMARY KEY,
    user_ID     CHAR(5) NOT NULL,
    Prod_Name   NCHAR(10) NOT NULL,
    Group_Name  NCHAR(10),
    Price       NUMBER(8) NOT NULL,
    Amount      NUMBER(3) NOT NULL,
    FOREIGN KEY (user_ID) REFERENCES USERTBL(user_ID)
);


